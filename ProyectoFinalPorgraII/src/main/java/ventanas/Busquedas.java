    public void Busqueda() {
        Connection conn = null;
        PreparedStatement stmt = null;
        ResultSet rs = null;
        Pokemones poke = null;
        boolean tiene_permiso = false;
        DefaultTableModel colum = new DefaultTableModel();
        String[] datos = new String[25];
        colum.addColumn("id");
        colum.addColumn("name");
        colum.addColumn("species");
        colum.addColumn("color");
        colum.addColumn("habitat");
        colum.addColumn("gender_rate");
        colum.addColumn("capture_rate");
        colum.addColumn("base_experience");
        colum.addColumn("forme_name");
        tableConsultas.setModel(colum);
        try {
            conn = ClsConexion.getConnection();
            stmt = conn.prepareStatement("SELECT * FROM test.pokemon where name = ?");
            stmt.setString(1, txtBuscarPorNombre.getText());
            rs = stmt.executeQuery();
            int contador = 0;
            while (rs.next()) {
                int id = Integer.parseInt(rs.getString("id"));
                dibujaElPokemonQueEstaEnLaPosicion(id - 1);
                contador++;
                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(12);
                datos[3] = rs.getString(13);
                datos[4] = rs.getString(15);
                datos[5] = rs.getString(16);
                datos[6] = rs.getString(17);
                datos[7] = rs.getString(18);
                datos[8] = rs.getString(3);
                colum.addRow(datos);
                resultadoConsulta = estado.
                        executeQuery("select * from pokemon where id = " + id);
                if (resultadoConsulta.next()) {
                    lblNombre.setText(resultadoConsulta.getString(2));
                } else {
                    lblNombre.setText("Este chucho no esta e el pokedex");
                }

            }
            JOptionPane.showMessageDialog(null, "Registros encontrados " + contador);

        } catch (SQLException ex) {
            ex.printStackTrace(System.out);
        } finally {
            ClsConexion.close(rs);
            ClsConexion.close(stmt);
            ClsConexion.close(conn);
        }
    }

